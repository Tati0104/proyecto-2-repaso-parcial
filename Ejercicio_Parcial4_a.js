let a = 3

while (a<=5){//El ciclo while continuará ejecutándose mientras la condición a <= 5 sea verdadera. 
    //Es decir, mientras a sea menor o igual a 5, el bloque de código dentro del while seguirá ejecutándose.
    a>4 ? console.log(2) : console.log(4);/*Este es un operador ternario que evalúa una condición
     y realiza una de dos acciones dependiendo de si la condición es verdadera o falsa.
    Condición: a > 4
    Si a es mayor que 4, se ejecuta console.log(2).
    Si a no es mayor que 4, se ejecuta console.log(4).*/
    a++; //aqui incrementamos uno a la varibale "a"
}